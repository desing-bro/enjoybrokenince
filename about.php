                <p>Sometimes the most delicious food happens by chance. During harvesting, grains of rice often break, resulting in smaller but equally delicious pieces of “Broken Rice.” Despite its unusual name, Broken Rice has become a key ingredient in some of today’s most popular Asian street foods. In addition to being softer, stickier, and more absorbent than its unbroken counterpart, it also cooks in half the time, allowing street-side vendors to save on fuel. These favorable traits of Broken Rice make it possible for slow flavors to be served fast at a low price. Fresh, quick and affordable food that can be eaten every day, Broken Rice is the inspiration for our entire menu.</p>
